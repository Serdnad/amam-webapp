export function translate(lang: string) {
	return (key: string) => {
		const components = key.split('.');

		let item = translations;
		for (const k of components) {
			item = item[k];
		}

		return item[lang];
	};
}

export function t(key: string, lang: string) {
	const components = key.split('.');

	let item = translations;
	for (const k of components) {
		item = item[k];
	}

	return item[lang];
}
